
/**
 * add - Function to add two numbers.
 * @a: First interget.
 * @b: Second integer.
 * Return: a + b
 */
int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - Function to subtract two numbers.
 * @a: First interget.
 * @b: Second integer.
 * Return: a - b
 */
int sub(int a, int b)
{
	return (a - b);
}

/**
 * mul - Function to multiply two numbers.
 * @a: First interget.
 * @b: Second integer.
 * Return: a * b
 */
int mul(int a, int b)
{
	return (a * b);
}

/**
 * div - Function to divide two numbers.
 * @a: First interget.
 * @b: Second integer.
 * Return: a / b
 */
int div(int a, int b)
{
	return (a / b);
}

/**
 * mod - Function to find the modulo of two numbers.
 * @a: First interget.
 * @b: Second integer.
 * Return: a % b
 */
int mod(int a, int b)
{
	return (a % b);
}
