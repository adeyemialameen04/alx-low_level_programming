#include <stdio.h>
#include "main.h"

/**
 * _puts - Print a string to the standard output
 * @str: Pointer to the string to be printed
 *
 * Description: This function takes as an argument a pointer to a string (str)
 * and uses the standard library function `puts` to print the string to the
 * standard output.
 */
void _puts(char *str)
{
puts(str);
}
